#tuple is immutable (can't change or update)
#list=[1,2,3,4]
#tuple=(1,2,3,4)


a=(1,2,3,4)
print(type(a)) #datatype is tuple

a=(1)
print(type(a)) #datatype is int (in this case if we want to define one element) terminal take this as integer
               #which is closed in parenthesis


a=(1,)
print(type(a)) #this is how we can define only one element in tuple 