# FOR LOOP 
# A for loop is used to iterate through a sequence like list, tuple, or string [iterables] 

#for list
list=[1,2,3,18,"radhe","kanha"]

for i in list:
    print(i)

#for tuple
t={1,2,3,45,67,"radhe"}

for i in t:
    print(i)


#for string
string="RadhaKrishna"

for i in string:
    print(i)